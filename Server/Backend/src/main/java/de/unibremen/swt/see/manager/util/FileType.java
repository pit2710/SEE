package de.unibremen.swt.see.manager.util;

/**
 * Represents the different types of files that can be uploaded to the
 * application.
 * <p>
 * This enum defines the file types that are used by the SEE game server to 
 * configure multiplayer scenarios and Code Cities.
 */
public enum FileType {
    SOURCE,
    GXL,
    CONFIG,
    SOLUTION,
    CSV
}
